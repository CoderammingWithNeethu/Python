def main():
    list1 = [3,2,5,0,1]

    #ANY
    #Any function will evaluate to True if one of the value evaluates to True
    print(any(list1))

    #ALL
    #All function will evaluate to True if all the values are True
    print(all(list1))

    #MIN 
    print(min(list1))

    #MAx
    print(max(list1))

    #SUM
    print(sum(list1))


if __name__ == '__main__':
    main()